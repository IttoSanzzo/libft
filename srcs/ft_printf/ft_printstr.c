#include "libft.h"

int	ft_printstr(char *str)
{
	int	i;

	if (!(str))
	{
		write(1, "(null)", 6);
		return (6);
	}
	i = -1;
	while (str[++i])
		write(1, &str[i], 1);
	return (i);
}
