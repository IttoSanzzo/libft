#include "libft.h"

int	ft_cend(char *s)
{
	int	i;

	i = ft_strlen(s) - 1;
	while (i >= 0 && s[i] == ' ')
		i--;
	if ((i >= 0) && ((s[i] == '|')
			|| (s[i] == '&' && (i > 0 && s[i - 1] == '&'))))
		return (1);
	return (0);
}
